public class Animal {
   String family;
   String name;
   int age;
   boolean isMammal;

   // Constructeur
   public Animal(String family, String name, int age, boolean isMammal) {
      this.family = family;
      this.name = name;
      this.age = age;
      this.isMammal = isMammal;
   }

   public String getFamily() {
      return family;
   }



   public String getName() {
      return name;
   }



   public int getAge() {
      return age;
   }






}
